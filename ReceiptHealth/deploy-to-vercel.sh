# Deploy ReceiptHealth React App to Vercel

# 1. Install Vercel CLI globally
npm install -g vercel

# 2. Navigate to the client folder
cd c:\VSCodeProjects\GitHub\copilot-powered-app\ReceiptHealth\client

# 3. Build the app
npm run build

# 4. Deploy to Vercel
vercel --prod

# Follow the prompts:
# - Login with GitHub/Google/Email
# - Confirm project settings
# - Get your live URL!
